u8 bls_smp_pushDataSplit (u8 smp_code, u8* p, u8 len)
{
	int n;
	u8 pkt_smp_short[36] = {0};

	n = len < (MTU_SIZE - 1) ? len : MTU_SIZE - 1;
	pkt_smp_short[0] = n + 7;
	pkt_smp_short[4] = 2;				//first data packet
	pkt_smp_short[5] = n + 5;			// rf_len

	*(u16*)(pkt_smp_short + 6) = len + 1;	//l2cap
	*(u16*)(pkt_smp_short + 8) = 0x06;	//chanid
	pkt_smp_short[10] = smp_code;

	memcpy (pkt_smp_short + 11, p, n);
	bls_ll_pushTxFifo (BLS_CONN_HANDLE | HANDLE_STK_FLAG, pkt_smp_short + 4);

	for (int i=n; i<len; i+=n)
	{
		n = (len - i) > (MTU_SIZE + 4) ? (MTU_SIZE + 4) : len - i;
		pkt_smp_short[0] = n + 2;
		pkt_smp_short[4] = 1;				//first data packet
		pkt_smp_short[5] = n;
		memcpy (pkt_smp_short + 6, p + i, n);
		bls_ll_pushTxFifo (BLS_CONN_HANDLE | HANDLE_STK_FLAG, pkt_smp_short + 4);
	}
	return len;
}





//push smp cmd data to send peer device
void blc_smp_sc_pushPkt_handler(int type)
{
	if(blc_smpMng.secure_conn) //smp4.2 or above
	{
		switch(type)
		{
			case SMP_OP_PAIRING_CONFIRM:
			{
				u32 sc_passkey = (smp_param_own.paring_tk[2] << 16 | smp_param_own.paring_tk[1] << 8 | smp_param_own.paring_tk[0] );
				/*core4.2 Vol3,Part H, Page2302
				    Z is zero (i.e. 8 bits of zeros) for Numeric Comparison and OOB protocol. In the
					Passkey Entry protocol, the most significant bit of Z is set equal to one and the
					least significant bit is made up from one bit of the passkey e.g. if the passkey
					bit is 1, then Z = 0x81 and if the passkey bit is 0, then Z = 0x80.*/
				u8 f4_param_z;

				//In Secure connections Passkey entry protocol.
				if(blc_smpMng.stk_method >= PK_Init_Dsply_Resp_Input && blc_smpMng.stk_method <= PK_BOTH_INPUT){
					f4_param_z = ((sc_passkey >> smpPkShftCnt) & 0x01) | 0x80;
					generateRandomNum(16, smpOwnRand_const);

					/*
					 * Secure Connection Pairing:
					 * M->S Pairing Req: phase1 begin
					 * S->M Pairing Rsp:
					 * M->S Pairing Public Key: phase2 begin
					 * S->M Pairing Public Key:
					 * ......
					 * M->S Pairing Confirm: (20 times)
					 * S->M Pairing Confirm: (20 times) Pairing Confirm marked
					 */
					if(smp_phase_record & SMP_PHASE_STAGE2 && (smpPkShftCnt == 19)){ //20 times confirm/random, idx from Zero.
						smp_phase_record |= BIT(SMP_OP_PAIRING_CONFIRM);
					}
				}
				else{//Numeric Comparison(or Just work) and OOB protocol( Note: OOB is not take into account)
					f4_param_z = 0;

					/*
					 * Secure Connection Pairing:
					 * M->S Pairing Req: phase1 begin
					 * S->M Pairing Rsp:
					 * M->S Pairing Public Key: phase2 begin
					 * S->M Pairing Public Key:
					 * S->M Pairing Confirm: Pairing Confirm marked
					 */
					if(smp_phase_record & SMP_PHASE_STAGE2){
						smp_phase_record |= BIT(SMP_OP_PAIRING_CONFIRM);
					}
				}
				//Cb = f4(PKb,PKa,Nb,0)
				blt_smp_alg_f4(smpOwnParingConfirm, smp_sc_key.sc_pk_own, smp_sc_key.sc_pk_peer, smpOwnRand_const, f4_param_z);
				swapN (smpOwnParingConfirm, 16);
			}
			break;

			case SMP_OP_PAIRING_RANDOM:
			{
				if(blc_smpMng.stk_method >= PK_Init_Dsply_Resp_Input && blc_smpMng.stk_method <= PK_BOTH_INPUT){
					/*
					 * Secure Connection Pairing:
					 * M->S Pairing Req: phase1 begin
					 * S->M Pairing Rsp:
					 * M->S Pairing Public Key: phase2 begin
					 * S->M Pairing Public Key:
					 * ......
					 * M->S Pairing Confirm: (20 times)
					 * S->M Pairing Confirm: (20 times) Pairing Confirm marked
					 * M->S Pairing Random: (20 times)
					 * S->M Pairing Random: (20 times) Pairing Random marked
					 */
					if(smp_phase_record & BIT(SMP_OP_PAIRING_CONFIRM) && (smpPkShftCnt == 20)){//20 times confirm/random
						smpPkShftCnt = 0;
						smp_phase_record |= BIT(SMP_OP_PAIRING_RANDOM);
					}
				}
				/* Note: OOB is not take into account
				 * Secure Connection Pairing:
				 * M->S Pairing Req: phase1 begin
				 * S->M Pairing Rsp:
				 * M->S Pairing Public Key: phase2 begin
				 * S->M Pairing Public Key:
				 * S->M Pairing Confirm: Pairing Confirm marked
				 * M->S Pairing Random:
				 * S->M Pairing Random: Pairing Random marked
				 */
				else if(smp_phase_record & BIT(SMP_OP_PAIRING_CONFIRM)){
					smp_phase_record |= BIT(SMP_OP_PAIRING_RANDOM);
				}
				swapX( smpOwnRand_const, smpResSignalPkt.data, 16);
			}
			break;
			case SMP_OP_PAIRING_PUBLIC_KEY:
			{
				u8 temp_data[64] = {0};
				swapX(smp_sc_key.sc_pk_own, temp_data, 32);
				swapX(smp_sc_key.sc_pk_own+32, temp_data+32, 32);
				bls_smp_pushDataSplit (SMP_OP_PAIRING_PUBLIC_KEY, temp_data, 64);  // need packet split
			}
			break;
			case SMP_OP_PAIRING_DHKEY:
			{
				/*
				 * Secure Connection Pairing:
				 * M->S Pairing Req: phase1 begin
				 * S->M Pairing Rsp:
				 * M->S Pairing Public Key: phase2 begin
				 * S->M Pairing Public Key:
				 * ......
				 * M->S Pairing Random:
				 * S->M Pairing Random: Pairing Random marked
				 * M->S Pairing DHKey Check: Pairing DHKey Check marked
				 */
				if(smp_phase_record & BIT(SMP_OP_PAIRING_RANDOM)){
					smp_phase_record |= BIT(SMP_OP_PAIRING_DHKEY);
				}
				smpResSignalPkt.l2capLen = 0x11;        //l2cap len
				swapX (smp_param_peer.paring_confirm, smpResSignalPkt.data, 16);

				//smp4.2 or above, after exchange smp pairing DH key check, then transport specific keys distribution
				smpDistirbuteKeyOrder = SMP_TRANSPORT_SPECIFIC_KEY_START;
			}
			break;

			default:
			break;
		}
	}
}

